# Create admin-only decorator
def admin_only(f):
    @wraps(f)
    # If id is not 1(or whatever is admin_id) then return abort with 403 error
    # As anonymous user doesn’t have id and Flask will keep raising exception that the current user doesn’t have the id
    # attribute, so also catch unauthenticated users through is_authenticated (part of UserMixin)
    def decorated_function(*args, **kwargs):
        if not current_user.is_authenticated or current_user.id != 1:
            return abort(403)
        # Otherwise continue with the route function
        return f(*args, **kwargs)
    return decorated_function


